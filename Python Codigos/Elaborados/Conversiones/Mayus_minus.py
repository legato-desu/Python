# Mayusculas y minusculas

"""
Crear un programa que cambie entre mayúsculas y minúsculas las palabras almacenadas en las variables
"""

small = "hola mundo"
mixed = "HOLA mundo"
big = "HOLA MUNDO"
small = small.upper() 
mixed = mixed.swapcase()  
big = big.lower() 
print(small)
print(mixed)
print(big)