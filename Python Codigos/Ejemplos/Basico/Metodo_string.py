# método String

"""
Cree un programa que utilice diferentes métodos de cadena para modificar texto.
"""

# UPER --> convertir todo a mayúsculas
city = "Bogota"
city = city.upper()
print(f"Upper: {city}")

# LOWER  --> convertir todo a minúsculas
country = "CANADA"
country = country.lower()
print(f"Lower: {country}")

# CAPIALIZE  --> Convertir la primera letra a mayúscula
color = "AZUL"
color = color.capitalize()
print(f"Capitalize: {color}")