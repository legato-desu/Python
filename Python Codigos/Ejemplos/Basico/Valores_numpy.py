# Valores numpy

import numpy as np
"""
Cree un programa que muestre los valores actualizados de la biblioteca numpy
"""

a = np.array([(1.5, 2, 3),(4, 5, 6)])
b = np.array([1, 2, 3])

# Exponencial (exp)
Exponential = np.exp(b)
print(f"Exponencial: {Exponential}")

# Ra√≠z cuadrada (sqrt)
root = np.sqrt(b)
print(f"Raiz: {root}")

# Seno  (sin)
Sine = np.sin(a)
print(f"Seno: {Sine}")

# Coseno (cos)
cosine =np.cos(a)
print(f"Coseno: {cosine}")

# Logaritmo naatural (log)
logarithm =np.log(b)
print(f"logaritmo: {logarithm}")