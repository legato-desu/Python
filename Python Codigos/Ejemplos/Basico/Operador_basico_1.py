a = 30
b = 3

# Suma
addition = a + b
print(f"suma: {a} + {b} = {addition}")

# Resta
subtract = a - b
print(f"Resta: {a} - {b} = {subtract}")

# Multiplicacion
multiplication = a * b
print(f"Multiplicacion: {a} x {b} = {multiplication}")

# Division
split = a / b
print(f"Division: {a} / {b} = {split:.1f}")

# Exponente
exponent = a ** b
print(f"Exponente: {a} ^ {b} = {exponent:,.0f}")

