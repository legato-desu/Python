# Uso Sort

"""
Crear un programa que ordene alfabéticamente los elementos de una lista
"""

country = ["Peru","Brasil","Argentina","Ecuador", "Uruguay", "Chile", "Bolivia", "Colombia", "Venezuela"]
print(country)

# metodo (SORT) forma alfabéticamente ascendente
country.sort()
print(country)

# metodo (SORT) forma alfabéticamente descendente 
country.sort(reverse=True)
print(country)